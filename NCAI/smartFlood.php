                <p>The project is about design and implementation of a system for predicting, monitoring and controlling floods using AI techniques. This project incorporates software and hardware design that enables wireless detection at sensor nodes which converge to a sensor gateway, transformation of data from the sensor nodes to a database located at a convenient site and managed from a central database, prediction of the possible flood threat and dissemination to the concerned authorities.  </p> <p>Flood occurs when a river bursts its banks and is normally caused by heavy rainfall and river overflow. The monitoring of down streams in different area joining a common river is important for early detection of flood. The speed with which the water from different sources drain into river is monitored using water flow sensors. The flow can be increased due to heavy rainfall, river blockage and tidal waves, sudden melting of ice when the temperature rises resulting in massive movement of water into places that is usually dry. The water bed in the rivers can hold a certain level of water. Water level sensors will be installed in rivers to monitor the overflow when the water level exceeds the defined threshold. Research will be carried out on different flood prediction models, their limitations and advantages. A novel artificial intelligence based flood prediction model will be presented that outperforms all the systems proposed till date. The data from water flow and level sensors reaches access module (AM) through ZigBee which is further transmitted to regional data center (RDC) and central data center (CDC) using GPRS. The prediction model takes as input the sensor values and forecast the possibility of flood in a certain area. </p>
              
