        <p>National Center of Artificial Intelligence (NCAI) was inaugurated at the main campus of National University of Sciences & Technology (NUST) at NUST on 16th of March, 2018​​. Speaking at the occasion, Minister for Interior and Planning, Development and Reform Mr Ahsan Iqbal emphasized the importance of Artificial Intelligence for modern Knowledge-based Economies. Encouraging youth to take up research and education in the fields relating to Artificial Intelligence and Robotics, he mentioned how the federal government’s laptop scheme has changed the knowledge landscape in Pakistan and brought Pakistan to one of the leading countries in the world in terms of Information Technologies-based entrepreneurship.​ </p> <p>
NCAI is the latest technological initiative of Government of Pakistan under the government’s Vision 2025. The center is designed to become the leading hub of innovation, scientific research, knowledge transfer to the local economy, and training in the area of Artificial Intelligence (AI) and its closely affiliated fields. The central aim is to facilitate the researchers in the field of AI; help them establish and grow AI industry following international trends and seek solutions to the indigenous problems through AI.</p>
<p>
Approved by the Government of Pakistan in January 2018, NCAI is designed on a consortium model where the most leading researchers in the field of AI are identified on competitive grounds and new state-of-the-art labs are established under their supervision. </p>
<p>
The labs and their respective Principal Investigators have been selected through a competitive evaluation and rigorous selection process carried out by Higher Education Commission (HEC) and Planning Commission of Pakistan. Out of all the chosen top researchers of Artificial Intelligence in Pakistan, NUST has been chosen as the best University on competitive grounds. </p>
<p>
In his remarks on the occasion, Lt Gen Naveed Zaman HI (M), (Retd) appreciated the joint efforts of team of researchers from 6 universities that worked in close collaboration for preparing the proposal of the National Center.</p>
<p>
The inauguration ceremony was attended by experts of Artificial Intelligence both from academia and industry as well as students of Artificial Intelligence and Robotics from various universities across Pakistan.</p>
